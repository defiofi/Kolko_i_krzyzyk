package kolkoIKrzyzyk.mechanika;

public class WinDate {
    private int win;
    private int winLine;

    public WinDate(int win, int winLine) {
        this.win = win;
        this.winLine = winLine;
    }

    public int getWin() {
        return win;
    }

    public void setWin(int win) {
        this.win = win;
    }

    public int getWinLine() {
        return winLine;
    }

    public void setWinLine(int winLine) {
        this.winLine = winLine;
    }
}
